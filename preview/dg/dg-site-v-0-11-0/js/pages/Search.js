  // Search class
class Search {
    constructor(products) {
        this.products = products;
        this.searchInput = document.getElementById('searchBar');
        this.searchIcon = document.getElementById('searchIcon');

        // Bind methods
        this.bindEvents();
    }

    bindEvents() {
        this.searchIcon.addEventListener('click', () => {
            const query = this.searchInput.value.trim();
            this.performSearch(query);
        });

        // Handle Enter key for search
        this.searchInput.addEventListener('keypress', (event) => {
            if (event.key === 'Enter') {
                const query = this.searchInput.value.trim();
                this.performSearch(query);
            }
        });
    }

    performSearch(query) {
        // Check if the search input is empty
        if (query === '') {
            // If empty, display all products
            displayProducts({ products });
            return;
        }

        // Filter products array based on the search query
        const filteredProducts = this.products.filter(product =>
            product.name.toLowerCase().includes(query.toLowerCase())
        );

        // Display filtered products
        displayProducts({ products: filteredProducts });
    }
}

// Function to display products
function displayProducts(productsObj) {
    const container = document.getElementById('product-container'); // Make sure this ID matches your HTML
    container.innerHTML = ''; // Clear previous products

    console.log(productsObj); // Check which products are being displayed
    productsObj.products.forEach(product => {
        const productCard = new ProductCard(product);
        container.innerHTML += productCard.render();
    });
}

